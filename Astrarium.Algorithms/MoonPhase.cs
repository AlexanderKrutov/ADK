namespace Astrarium.Algorithms
{
    /// <summary>
    /// Defines four main phases of the Moon
    /// </summary>
    public enum MoonPhase : int
    {
        /// <summary>
        /// New Moon
        /// </summary>
        NewMoon         = 0,

        /// <summary>
        /// First Quarter
        /// </summary>
        FirstQuarter    = 25,

        /// <summary>
        /// Full Moon
        /// </summary>
        FullMoon        = 50,

        /// <summary>
        /// Last Quarter
        /// </summary>
        LastQuarter     = 75
    }
}
