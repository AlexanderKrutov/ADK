namespace Astrarium.Algorithms
{
    /// <summary>
    /// Visibility regio of the solar eclipse
    /// </summary>
    public enum SolarEclipseRegio
    {
        /// <summary>
        /// Eclipse is primarily visible in Northern hemisphere
        /// </summary>
        Northern = 1,

        /// <summary>
        /// Eclipse is primarily visible in equatorial regio
        /// </summary>
        Equatorial = 0,

        /// <summary>
        /// Eclipse is primarily visible in Southern hemisphere
        /// </summary>
        Southern = -1
    }
}
