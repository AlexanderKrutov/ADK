namespace Astrarium.Algorithms
{
    /// <summary>
    /// Defines astronomical seasons
    /// </summary>
    public enum Season
    {
        /// <summary>
        /// Beginning of astronomical spring, i.e. vernal equnox
        /// </summary>
        Spring,

        /// <summary>
        /// Beginning of astronomical summer, i.e. summer solstice
        /// </summary>
        Summer,

        /// <summary>
        /// Beginning of astronomical autumn, i.e. autumnal equnox
        /// </summary>
        Autumn,

        /// <summary>
        /// Beginning of astronomical winter, i.e. winter solstice
        /// </summary>
        Winter
    }
}
